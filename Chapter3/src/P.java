public class P {
    int size;
    int toppings;
    double total;
    public void setSize(int a)
    {
        size = a;
    }
    public void setToppings(int b)
    {
        toppings = b;
    }
    public void setTotal(double c)
    {
        total = c;
    }
    public int getSize()
    {
        return size;
    }
    public int getToppings()
    {
        return toppings;
    }
    public double getTotal()
    {
        return total;
    }
}
